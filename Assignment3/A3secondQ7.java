class A3secondQ7{
	public static void main(String args[]){
		for(int i=1;i<=5;i++){
			for(int j=5;j>=i;j--){
				System.out.print(" ");
			}
			for(int k=1;k<=i;k++){
				System.out.print("*");
			}
			for(int m=2;m<=i;m++){
				System.out.print("*");
			}
		System.out.println();}
		
		
		for(int a=1;a<=4;a++){
			for(int b=0;b<=a;b++){
				System.out.print(" ");
			}
			for(int c=4;c>=a;c--){
				System.out.print("*");
			}
			for(int d=3;d>=a;d--){
				System.out.print("*");
			}
		System.out.println();}
		
	}
}