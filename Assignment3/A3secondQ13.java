class A3secondQ13{
	public static void main(String args[]){
		
		for(int i=1;i<=5;i++){
			for(int j=2;j<=i;j++){
				System.out.print(" ");
			}
			for(int k=5;k>=i;k--){
				System.out.print("*");
			}
		System.out.println();}
		
		
		
		for(int m=1;m<=4;m++){
			for(int n=3;n>=m;n--){
				System.out.print(" ");
			}
			for(int k=0;k<=m;k++){
				System.out.print("*");
			}
	    System.out.println();	}
		
	}
}